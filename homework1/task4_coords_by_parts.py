"""Определение диапазонов возможных координат точек в заданной четверти (x и y) по ее номеру"""

quater_num = 0

#цикл для проверки того, что пользователь не допустил ошибку и ввел номер четверти
while quater_num not in range(1,5):
    quater_num = int(input('Введите номер четверти (1-4) '))

x = 'x > 0' if quater_num in [1,4] else 'x < 0'
y = 'y > 0' if quater_num in [1,2] else 'y < 0'
print(f'Возможные координаты точек в {quater_num}-ой четверти:{x} {y}')
